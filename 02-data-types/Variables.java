
class Variables {
	public static void main(String[] args) {
		byte b;
		b = 5;
		System.out.println(b);		
		b = 25;
		System.out.println(b);		
		// b = 130;
		b = 127;
		System.out.println(b);		
		
		char c = 'A';
		System.out.println(c);		
		
		short x = 130;
		System.out.println(x);		
		int y = 147570;
		System.out.println(y);		
		float z = 25.36f;
		System.out.println(z);
	}
}